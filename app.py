    # 1. Filtramos solo los últimos 2 años de datos para ver la actualidad real
    fecha_limite = pd.Timestamp.now() - pd.DateOffset(years=2)
    df_reciente = df_historico[df_historico['date'] >= fecha_limite].copy()

    # 2. Definimos la importancia del torneo (Pesos de Torneo)
    def obtener_peso_torneo(tournament):
        tournament = str(tournament).lower()
        if 'world cup' in tournament or 'copa américa' in tournament or 'euro' in tournament:
            return 2.5
        elif 'qualification' in tournament or 'nations league' in tournament:
            return 1.8
        elif 'friendly' in tournament:
            return 0.8
        else:
            return 1.0

    df_reciente['Peso_Torneo'] = df_reciente['tournament'].apply(obtener_peso_torneo)

    # 3. Función analítica para calcular la forma ponderada en el tiempo
    def calcular_forma_temporal_avanzada(pais):
        partidos = df_reciente[(df_reciente['home_team'] == pais) | (df_reciente['away_team'] == pais)].copy()

        if len(partidos) == 0:
            return 0.5

        pesos_totales = 0
        puntos_ponderados = 0
        fecha_actual = pd.Timestamp.now()

        for idx, row in partidos.iterrows():
            antiguedad_meses = (fecha_actual - row['date']).days / 30.44
            peso_tiempo = np.exp(-0.1 * antiguedad_meses)
            peso_final_partido = peso_tiempo * row['Peso_Torneo']

            if row['home_team'] == pais:
                goles_favor = row['home_score']
                goles_contra = row['away_score']
            else:
                goles_favor = row['away_score']
                goles_contra = row['home_score']

            if goles_favor > goles_contra:
                puntos = 3
            elif goles_favor == goles_contra:
                puntos = 1
            else:
                puntos = 0

            puntos_ponderados += (puntos / 3) * peso_final_partido
            pesos_totales += peso_final_partido

        if pesos_totales == 0: return 0.5
        return puntos_ponderados / pesos_totales

    df_mundial['Estado_Forma'] = df_mundial['Equipo'].apply(calcular_forma_temporal_avanzada)
    print("✓ Estado de forma con decaimiento temporal y peso de torneos integrado con éxito.")

