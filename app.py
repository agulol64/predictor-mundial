    def generar_probabilidades(l1, l2):
        max_goles = 7
        matriz = np.zeros((max_goles, max_goles))
        for g1 in range(max_goles):
            for g2 in range(max_goles):
                matriz[g1, g2] = poisson.pmf(g1, l1) * poisson.pmf(g2, l2)

        g1 = np.sum(np.tril(matriz, k=-1)) * 100
        x = np.sum(np.diag(matriz)) * 100
        g2 = np.sum(np.triu(matriz, k=1)) * 100

        resultados = []
        for r1 in range(max_goles):
            for r2 in range(max_goles):
                resultados.append(((r1, r2), matriz[r1, r2] * 100))
        resultados.sort(key=lambda x: x[1], reverse=True)
        return g1, x, g2, resultados[:3]

    g1_b, x_b, g2_b, top3_base = generar_probabilidades(lambda_e1_base, lambda_e2_base)

    brecha_ranking = abs(datos_e1['Ranking_FIFA'] - datos_e2['Ranking_FIFA'])
